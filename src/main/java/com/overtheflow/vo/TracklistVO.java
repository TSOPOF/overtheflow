package com.overtheflow.vo;

public class TracklistVO {
	private int tracklistOrder;
	private int playlistNo;
	private int fileNo;
	
	public int getTracklistOrder() {
		return tracklistOrder;
	}
	public void setTracklistOrder(int tracklistOrder) {
		this.tracklistOrder = tracklistOrder;
	}
	public int getPlaylistNo() {
		return playlistNo;
	}
	public void setPlaylistNo(int playlistNo) {
		this.playlistNo = playlistNo;
	}
	public int getFileNo() {
		return fileNo;
	}
	public void setFileNo(int fileNo) {
		this.fileNo = fileNo;
	}
}
